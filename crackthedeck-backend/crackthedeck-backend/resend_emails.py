# ---------------------------------------------------------------------------
# Contact form endpoint
# ---------------------------------------------------------------------------

SUPPORT_EMAIL = os.getenv("SUPPORT_EMAIL", "alekseyposternak@gmail.com")

def _log_contact_db(name, email, message):
    try:
        from admin_db import log_contact
        log_contact(name=name, email=email, message=message)
    except Exception:
        pass

@router.post("/contact")
async def contact_form(body: ContactFormRequest):
    """
    Receive a support/contact message and forward it to the team email.
    """
    if not resend.api_key:
        logger.error("RESEND_API_KEY not set")
        raise HTTPException(500, "Email service not configured")

    if not body.name.strip() or not body.message.strip():
        raise HTTPException(400, "Name and message are required")

    # Limit message length
    msg_text = body.message.strip()[:5000]
    sender_name = body.name.strip()[:200]

    html_body = f"""
    <div style="font-family: -apple-system, BlinkMacSystemFont, 'Segoe UI', Roboto, sans-serif; max-width: 600px; margin: 0 auto; padding: 32px 24px;">
        <h2 style="font-size: 20px; font-weight: 600; color: #111; margin: 0 0 16px;">New Support Message</h2>
        <div style="background: #f9fafb; border-radius: 12px; padding: 20px; margin-bottom: 16px;">
            <p style="margin: 0 0 8px; font-size: 14px;"><strong>From:</strong> {sender_name} &lt;{body.email}&gt;</p>
            <p style="margin: 0; font-size: 14px; white-space: pre-wrap; line-height: 1.6;">{msg_text}</p>
        </div>
        <p style="color: #888; font-size: 12px;">Sent via CrackTheDeck contact form</p>
    </div>
    """

    try:
        params = {
            "from": FROM_EMAIL,
            "to": [SUPPORT_EMAIL],
            "reply_to": body.email,
            "subject": f"[CrackTheDeck Support] Message from {sender_name}",
            "html": html_body,
        }
        result = resend.Emails.send(params)
        logger.info(f"Contact form from {body.email}, resend_id={result.get('id', 'unknown')}")
        _log_contact_db(sender_name, body.email, msg_text)
        return JSONResponse({
            "success": True,
            "message": "Message sent successfully",
        })

    except Exception as e:
        logger.error(f"Failed to send contact form from {body.email}: {e}")
        raise HTTPException(502, f"Failed to send message: {str(e)}")
